  <h1>Syarat & Ketentuan Pengguna - KerjaSetara</h1>
  <p><em>Dokumen ini mengatur ketentuan penggunaan platform KerjaSetara untuk penyandang disabilitas.</em></p>

  <h2>1. Pendaftaran Akun</h2>
  <p>1.1 Pengguna harus mendaftar dan membuat akun di platform kami dengan menyediakan informasi yang akurat dan lengkap.</p>
  <p>1.2 Pengguna yang mendaftar harus berusia minimal 18 tahun atau memiliki izin dari wali/penjaga sah jika berusia di bawah 18 tahun.</p>

  <h2>2. Kualifikasi Pencari Kerja</h2>
  <p style="margin-bottom: 0;">2.1 Aplikasi ini khusus disediakan untuk penyandang disabilitas yang memenuhi kriteria sebagai berikut:</p>
  
  <ol style="margin-left: 30px; margin-top: 0;" type="a">
    <li>Penyandang disabilitas yang memiliki hambatan fisik, mental, atau sensorik.</li>
    <li>Penyandang disabilitas yang dapat bekerja dengan dukungan yang sesuai dengan kebutuhan mereka, termasuk modifikasi tempat kerja atau alat bantu.</li>
    <li>Pengguna diminta untuk mengisi informasi terkait jenis disabilitas yang dimiliki agar kami dapat menyesuaikan rekomendasi pekerjaan dengan kebutuhan khusus.</li>
  </ol>
  
  <p style="margin-top: 0;">2.2 Penyandang disabilitas yang sedang mencari pekerjaan harus memiliki kualifikasi dan keterampilan yang relevan dengan lowongan pekerjaan yang tersedia di platform kami.</p>
  

  <h2>3. Informasi Pencari Kerja</h2>
  <p>3.1 Pengguna diharuskan untuk memberikan informasi yang benar, lengkap, dan terbaru mengenai riwayat pendidikan, pengalaman kerja, keterampilan, dan disabilitas yang dimiliki.</p>
  <p>3.2 Informasi yang diberikan oleh pengguna akan digunakan untuk memproses aplikasi kerja dan menyediakan pekerjaan yang sesuai dengan kemampuan dan kebutuhan penyandang disabilitas.</p>
  <p>3.3 Pengguna dapat mengunggah dokumen pendukung, seperti surat keterangan medis atau dokumen relevan lainnya yang menunjukkan jenis disabilitas, jika diperlukan oleh perusahaan atau pemberi kerja.</p>

  <h2>4. Proses Aplikasi Pekerjaan</h2>
  <p>4.1 Pengguna dapat mengajukan lamaran pekerjaan melalui aplikasi dengan memilih pekerjaan yang sesuai berdasarkan kualifikasi dan minat.</p>
  <p>4.2 Setiap lamaran pekerjaan akan diproses dan disaring oleh pemberi kerja berdasarkan keterampilan, pengalaman, dan kemampuan yang sesuai dengan posisi yang dibutuhkan.</p>
  <p>4.3 Penyandang disabilitas yang melamar pekerjaan akan mendapatkan akses ke fitur-fitur khusus yang memungkinkan mereka untuk menyesuaikan pekerjaan dengan kebutuhan disabilitas mereka (misalnya, pengaturan tempat kerja atau alat bantu).</p>

  <h2>5. Privasi dan Perlindungan Data</h2>
  <p>5.1 Kami berkomitmen untuk menjaga privasi dan kerahasiaan data pribadi pengguna. Semua data yang diberikan akan dikelola sesuai dengan kebijakan privasi kami yang dapat diakses oleh pengguna kapan saja.</p>
  <p>5.2 Kami hanya akan membagikan informasi pribadi pengguna kepada pemberi kerja dengan persetujuan pengguna dan untuk tujuan pemrosesan lamaran pekerjaan.</p>
  <p>5.3 Pengguna dapat mengakses, memperbarui, atau menghapus data pribadi mereka kapan saja melalui pengaturan akun.</p>

  <h2>6. Tanggung Jawab Pemberi Kerja</h2>
  <p>6.1 Pemberi kerja yang memposting lowongan pekerjaan di platform ini harus berkomitmen untuk memberikan lingkungan kerja yang inklusif bagi penyandang disabilitas.</p>
  <p>6.2 Pemberi kerja diharapkan untuk mematuhi peraturan ketenagakerjaan yang berlaku, termasuk memberikan kesempatan yang setara bagi penyandang disabilitas.</p>
  <p>6.3 Pemberi kerja wajib menyediakan fasilitas dan dukungan yang sesuai untuk memungkinkan penyandang disabilitas menjalankan pekerjaan dengan maksimal.</p>

  <h2>7. Penggunaan Aplikasi</h2>
  <p>7.1 Pengguna setuju untuk menggunakan aplikasi ini hanya untuk tujuan yang sah dan sesuai dengan hukum yang berlaku.</p>
  <p>7.2 Pengguna dilarang untuk memposting informasi yang menyesatkan, diskriminatif, atau merugikan pihak lain.</p>
  <p>7.3 Kami berhak untuk menangguhkan atau menutup akun pengguna yang melanggar ketentuan penggunaan aplikasi ini.</p>

  <h2>8. Ketersediaan dan Pemeliharaan Aplikasi</h2>
  <p>8.1 Kami berusaha menjaga agar platform ini selalu tersedia dan berfungsi dengan baik, namun tidak dapat menjamin bahwa aplikasi akan bebas dari gangguan atau kesalahan.</p>
  <p>8.2 Pengguna harus menyadari bahwa pemeliharaan sistem atau pembaruan dapat menyebabkan gangguan sementara dalam penggunaan aplikasi.</p>

  <h2>9. Penghentian Layanan</h2>
  <p>9.1 Kami berhak untuk menangguhkan atau menghentikan akun pengguna apabila ditemukan pelanggaran terhadap syarat dan ketentuan ini.</p>
  <p>9.2 Pengguna dapat menutup akun mereka kapan saja melalui pengaturan akun jika tidak ingin melanjutkan penggunaan platform ini.</p>

  <h2>10. Pembaruan Syarat dan Ketentuan</h2>
  <p>10.1 Kami berhak untuk mengubah atau memperbarui syarat dan ketentuan ini kapan saja tanpa pemberitahuan sebelumnya.</p>
  <p>10.2 Pengguna disarankan untuk memeriksa syarat dan ketentuan ini secara berkala agar tetap mendapatkan informasi terbaru.</p>

